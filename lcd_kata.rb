class ConvertNumber
  DIGITS = [
    [[' ', '_', ' '], ['|', ' ', '|'], ['|', '_', '|']],
    [[' ', ' ', ' '], ['|', ' ', ' '], ['|', ' ', ' ']],
    [[' ', '_', ' '], [' ', '_ ','|'], ['|', '_', ' ']],
    [[' ', '_', ' '], [' ', '_', '|'], [' ', '_', '|']],
    [[' ', ' ', ' '], ['|', '_', '|'], [' ', ' ', '|']],
    [[' ', '_', ' '], ['|', '_', ' '], [' ', '_', '|']],
    [[' ', '_', ' '], ['|', '_', ' '], ['|', '_', '|']],
    [[' ', '_', ' '], [' ', ' ', '|'], [' ', ' ', '|']],
    [[' ', '_', ' '], ['|', '_', '|'], ['|', '_', '|']],
    [[' ', '_', ' '], ['|', '_', '|'], [' ', '_', '|']]
  ]

	attr_reader :number

  def initialize(number)
    @number = number
  end

  def number?
  	(number.to_i == 0) ? false : true
  end
  
  def display_lcd
    DIGITS[number]
  end
  
end

